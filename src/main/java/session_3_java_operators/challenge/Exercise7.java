package session_3_java_operators.challenge;

public class Exercise7 {

    public static void main(String[] args) {
        // 7. Write a program where you perform operations between an integer and a double.
        // Display the result.

        int no1 = 3;
        double no2 = 8;

        System.out.println("Sum: " + (no1 + no2));
        System.out.println("Difference: " + (no1 - no2));
        System.out.println("Product: " + (no1 * no2));
        System.out.println("Division: " + (no1 / no2));
        System.out.println("Modulus: " + (no1 % no2));
    }


}
